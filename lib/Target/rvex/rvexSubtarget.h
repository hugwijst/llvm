#ifndef rvexSUBTARGET_H
#define rvexSUBTARGET_H

#include "rvexFrameLowering.h"
#include "rvexInstrInfo.h"
#include "rvexISelLowering.h"
#include "rvexSelectionDAGInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetSubtargetInfo.h"
#include "llvm/MC/MCInstrItineraries.h"
#include <string>

namespace llvm {
class StringRef;

class rvexSubtarget : public TargetSubtargetInfo {
  virtual void anchor();

  bool IsVLIWEnabled;
  InstrItineraryData InstrItins;

  MCSchedModel SchedModel;
  const DataLayout    DL; // Calculates type size & alignment
  const rvexInstrInfo       InstrInfo; //- Instructions
  const rvexTargetLowering  TLInfo; //- Stack(Frame) and Stack direction
  const rvexSelectionDAGInfo TSInfo; // Selection DAG information
  const rvexFrameLowering   FrameLowering; //- Stack(Frame) and Stack direction

public:

  bool isVLIWEnabled() const { return IsVLIWEnabled; }

  /// enablePostRAScheduler - True at 'More' optimization.
  // bool enablePostRAScheduler(CodeGenOpt::Level OptLevel,
  //                            TargetSubtargetInfo::AntiDepBreakMode& Mode,
  //                            RegClassVector& CriticalPathRCs) const;
  
  // NOTE: O64 will not be supported.
  enum rvexABIEnum {
    UnknownABI, O32
  };

protected:
  enum rvexArchEnum {
    rvex32
  };

  // rvex architecture version
  rvexArchEnum rvexArchVersion;

  // rvex supported ABIs
  rvexABIEnum rvexABI;

  // IsLittle - The target is Little Endian
  bool IsLittle;

  // isLinux - Target system is Linux. Is false we consider ELFOS for now.
  bool IsLinux;

  

public:
  unsigned getTargetABI() const { return rvexABI; }

  /// This constructor initializes the data members to match that
  /// of the specified triple.
  rvexSubtarget(const std::string &TT, const std::string &CPU,
                const std::string &FS, bool little,
                rvexTargetMachine &TM);

  const InstrItineraryData *getInstrItineraryData() const override { return &InstrItins; }

  const DataLayout *getDataLayout() const override { return &DL;}
  const rvexInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const TargetFrameLowering *getFrameLowering() const override { return &FrameLowering; }

  const rvexRegisterInfo *getRegisterInfo() const override {
      return &InstrInfo.getRegisterInfo();
  }

  const rvexTargetLowering *getTargetLowering() const override { return &TLInfo; }

  const rvexSelectionDAGInfo* getSelectionDAGInfo() const override { return &TSInfo; }

//- Vitual function, must have
  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);


  /// createDFAPacketizer - We want to create our own packetizer table, as we
  /// populate it dynamically based on the core configuration specified by the
  /// user.
  //unsigned resolveSchedClass(unsigned SchedClass, const MachineInstr *DefMI, const TargetSchedModel *SchedModel) const override;
  DFAPacketizer *createDFAPacketizer(const InstrItineraryData *IID) const;

  bool isLittle() const { return IsLittle; }
  bool isLinux() const { return IsLinux; }
};
} // End llvm namespace

#endif
